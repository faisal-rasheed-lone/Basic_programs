//write a program to generate a sequence of numbers in both ascending and descending order
#include <iostream>

using namespace std;

int main()
{


    //printing numbers in ascending order
    cout<<"Numbers in ascending order  "<<endl;
    for(int i=1;i<10;i++)
        cout<<i<<" ";

    cout<<endl;

    //printing numbers in descending order
    cout<<"Numbers in descending order "<<endl;
    for(int i=10;i>0;i--)
        cout<<i<<" ";
}
